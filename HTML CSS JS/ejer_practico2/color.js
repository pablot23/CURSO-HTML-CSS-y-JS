
let color=prompt("ingresar un color primario: ");
switch (color) {
    case "rojo":
        console.log("es rojo");
        break;
    case "azul":
        console.log("es azul");
        break;
    case "amarillo":
        console.log("es amarillo");
        break;
    default:
        console.log("el color ingresado no es valido")
        break;
}